<p><span class="lbg_subtitle"><a name="particularities" id="particularities"></a>Particularities</span></p>
<p>If you have in the top of your page </p>
<p>&lt;!DOCTYPE html&gt;</p>
<p> the line height of elegant playlist will not be modified for responsive version. There are 2 solutions:</p>
<p>Solution 1. Remove &lt;!DOCTYPE html&gt;<br />
  Solution 2: Replace it with &lt;!DOCTYPE html PUBLIC &quot;-//W3C//DTD XHTML 1.0 Transitional//EN&quot; &quot;http://www.w3.org/TR/xhtml1/DTD/xhtml1-transitional.dtd&quot;&gt; </p>
